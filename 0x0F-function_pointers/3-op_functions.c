#include "3-calc.h"

/**
 * op_add - A function that adds two integers.
 * @a: Integer parameter.
 * @b: Integer parameter.
 *
 * Return: Sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - A function that give the difference between two integers.
 * @a: Integer parameter.
 * @b: Integer parameter.
 *
 * Return: Difference of b from a.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - A function that multiply two integers.
 * @a: Integer parameter.
 * @b: Integer parameter.
 *
 * Return: Multiplication of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - A function that gives the quotient two integers.
 * @a: Integer parameter.
 * @b: Integer parameter.
 *
 * Return: a / b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_add - A function that adds two integers.
 * @a: Integer parameter.
 * @b: Integer parameter.
 *
 * Return: Sum of a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
